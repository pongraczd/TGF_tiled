function [V]=Tesseroid_g(lon1,lat1,h1,ref,lon,lat,r0,resolution_x1,resolution_y1,e)
%% compute topographic potential of finite element -- tesseroid
Pi=3.141592654;
d2r=Pi/180;
G = 6.67384e-3;   % Universal Gravitational constant


%% build tesseroid
  zz1=lon1-resolution_x1/2;
  zz2=lon1+resolution_x1/2;
  yy1=lat1-resolution_y1/2;
  yy2=lat1+resolution_y1/2;
  xx1=h1;
  xx2=ref; 
 r1=(xx1+xx2)/2;
 %% variable definition 
 lat=lat*d2r;
 lat1=lat1*d2r;
 coslat=cos(lat);
 coslat1=cos(lat1);
 sinlat=sin(lat);
 sinlat1=sin(lat1);
d_lon=(lon1-lon)*d2r;
cosdlon=cos(d_lon);
sindlon=sin(d_lon);
cosphicosa=coslat1.*cosdlon;
coslatsinlat1=coslat.*sinlat1;
sinphicosa=coslatsinlat1-sinlat.*cosphicosa;
sinphisina=coslat1.*sindlon;
cosphi=sinlat.*sinlat1+coslat.*cosphicosa;
dx=r1.*sinphicosa;
dy=r1.*sinphisina;
dz=r1.*cosphi-r0;
dxx=dx.*dx;
dyy=dy.*dy;
dzz=dz.*dz;
l=sqrt(dxx+dyy+dzz);
xx=xx2-xx1;
yy=(yy2-yy1)*d2r;
zz=(zz2-zz1)*d2r;
c=G*e.*zz.*xx.*yy;
l2=l.*l;
%% Toi
r12=r1.*r1;
toi=r12.*coslat1;

toi1=2*r1.*coslat1;
toi2=-r12.*sinlat1;
toi3=0;

toi12=2.*coslat1;
toi22=-toi;
toi32=0;

%% dxi

x3_2=r1.*(sinlat.*coslat1-coslatsinlat1.*cosdlon);
x3_22=-r1.*cosphi;
x2_3=r1.*cosphicosa;

x3_3=-(dy).*coslat;
x3_32=-x2_3.*coslat;
%% ml
ml1=r0.*cosphi-r1;
ml2=r0.*x3_2;
ml3=r0.*x3_3;

ml12=-1;
ml22=r0.*x3_22;
ml32=r0.*x3_32;
%% a
l23=3./l2;

%% b
b1=2*ml1;
b2=2*ml2;
b3=2*ml3;

%% gama
ml1_2=ml1.*ml1;
ml2_2=ml2.*ml2;
ml3_2=ml3.*ml3;
gama1=ml12+l23.*ml1_2;
gama2=ml22+l23.*ml2_2;
gama3=ml32+l23.*ml3_2;

%% topographical potential
xx_2=(xx).*xx;
yy_2=(yy).*yy;
zz_2=(zz).*zz;
K000=1./l.*toi;
l3=1./l.^3;
g=c.*(K000+1/24.*l3.*(xx_2.*(toi12.*l2+toi1.*b1+toi.*gama1)+yy_2.*(toi22.*l2+toi2.*b2+toi.*gama2)+zz_2.*(toi32.*l2+toi3.*b3+toi.*gama3)));
V=sum(g(:))*1e-5;

end

