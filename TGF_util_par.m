function [prism_x1, prism_x2, prism_y1, prism_y2, prism_z1, prism_z2, prism_lon, prism_lat, prism_r, prism_e]=tess2prism(x1,x2,y1,y2,d1,d2,e)
% transform a tesseroid to a mass-equavilent prism
Pi=3.141592654;
d2r=Pi/180;

r1=d1;
r2=d2;
r0 = 0.5*(r1 + r2);

dx=r0*d2r.*(y2-y1);
dy=r0.*cos(d2r*0.5*(y1+y2))*d2r.*(x2-x1);
prism_x1=-0.5*dx;
prism_x2=-prism_x1;
prism_y1=-0.5*dy;
prism_y2=-prism_y1;
prism_z1=r1;
prism_z1(1:end)=(r2-r1)/2;
prism_z2=(r1-r2)/2;

tess_volume=d2r.*(x2-x1).*(r1.^3-r2.^3).*(sin(d2r*y2)-sin(d2r*y1))/3;
prism_volume=(prism_x2-prism_x1).*(prism_y2-prism_y1).*(prism_z2-prism_z1);

prism_e=e.*tess_volume./prism_volume;
prism_lon=0.5*(x1+x2);
prism_lat=0.5*(y1+y2);
prism_r=r0;
end

