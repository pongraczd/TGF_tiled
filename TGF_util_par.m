% The full-scale topographic gravitational field calculated via the TGF software 
% is generated by the topographic masses boundied by the mean sea level and the 
% Earth surface, while the high-frequency RTM gravity field is caused by the 
% attraction of residual masses between the Earth's surafce and a reference 
% surface. 
%
% In the TGF software, the topographic masses around the calculation point are 
% divided into four zones for RTM gravity field calculation and five zones for 
% full-scale topographic gravity field. The mass elements of nearest zones around 
% calculation points are approximated by polyhedron and prism for precise 
% computation, while tesseroid and point-mass for efficient computation for the
% far-zone masses. Please refer to following papers for detailed information about 
% combination  of various mass elements, and numerical evaluation of gravity field 
% generated by polyhedron, tesseroid, and point mass.

%% ---------------------- Input parameters -----------------------------------------
% ComPoints      --- in text format, list of number of points and triplet of the 
%                    spherical/ellipsoidal coordinates (lat[-90, 90], 
%                    lon [-180, 180], height [in meters])
% DetailedDEM    --- Detailed DEM
%                    --- given as a struct struct.DEM - DEM matrix,
%                    struct.meta containing [minlat maxlat minlon maxlon reslat reslon]
% DetailedREF    --- RTM reference surface with same resolution and format as DetailedDEM
% MassDensity    --- Map of mass density values, same resolution and format as DetailedDEM
%                    --- (from nearest neigb.)
%                    --- Only used when not a constant value is selected
%                    --- Unit g/cm^3
% TessDEM        --- DEM for tessroid zone      with same resolution and format as DetailedDEM
% TessREF        --- REF for tessroid zone      with same resolution and format as DetailedDEM
% TessDensity    --- mass density model for tessroid zone       with same resolution and format as DetailedDEM
% CoarseDEM      --- lower resolution DEM for far-zone , given as a
%                    filename, GeoTiff or other common GIS raster format
% CoarseREF      --- lower resolution REF for far-zone , given as a
%                    filename, GeoTiff or other common GIS raster format
% CoarseDensity  --- lower resolution mass density model for far-zone , given as a
%                    filename, GeoTiff or other common GIS raster format
% GlobalDEM      --- Global DEM for full-scale topographic gravity field calculations, given as a
%                    filename, GeoTiff or other common GIS raster format   
% GlobalREF      --- Global reference DEM for full-scale topographic gravity field, given as a
%                    filename, GeoTiff or other common GIS raster format 
% calculations (mean sea level) In full-scale topographic gravity field calculations, 
% & constant density assumption is made in TGF software for GlobalDEM covred area.


% idensity  --- flag for mass density with values of 0, 1
%           --- 0, constant value is used
%           --- 1, density map is used

% ikind  --- flag for the type of modelling with values of 1, 2
%        --- 1, Topographic gravitational field
%        --- 2, RTM gravitational field

% itype  --- Specification of field functionals with values of 0, 1, 2, 4, 10, 103, 104
%        --- 0, height anomaly / geoid height (N) in cm  
%        --- 1, Dovs (arc-sec) and gravity disturbances (mGal)
%        --- 4, Dovs (arc-sec) and gravity anomaly (mGal)
%        --- 2, all gradients (6 elements) in E
%        --- 10, all functionals (geoid, Dovs, gravity disturbances, gradient tensor)
%        --- 103, geoid height, Dovs and gravity disturbances
%        --- 104, geoid height, Dovs and gravity anomaly

% rzones --- integral radius specifying the computation zones rzones = [r1 r2 r3 r4]
%        --- r1, radius for polyhedron (detailed DEM)
%        --- r2, radius for prism (detailed DEM)
%        --- r3, radius for tesseroid (Tesseroid DEM)
%        --- r4, radius for point-mass (coarse DEM)

% flag_earth --- flag of earth approximation with values of 0, 1
%            --- 0, spherical approximation
%            --- 1, ellipsoidal approximation

% e      --- mass density in g/cm^3 when constant density assumption is used

%% ------------------------ Output parameters --------------------------------------
% height anomaly in unit of meter, Dov in unit of arc-sec, dg in unit of
% mGal, gradients in unit of E


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function TGF_util_par(ComPoints, DetailedDEM, DetailedREF, MassDensity, TessDEM, TessREF, TessDensity, CoarseDEM, CoarseREF, CoarseDensity,GlobalDEM,GlobalREF, outname,ikind, itype,idensity, flag_earth,rzones, e)
path=pwd;
addpath('bin')  
%R1=0.8; G1=0.8; B1=0.8;
%R2=0.95; G2=0.95; B2=0.95;
 
if idensity==1 
   if isempty(MassDensity) | isempty(CoarseDensity)
       error('please input a density model while not a constant value is selected');
   end
end

if idensity==0
   if isempty(e)
       error('please input constant density value while a constant value is selected');
   end
end  

if ikind~=1 & ikind~=2
   error('Please check ikind value: ikind can only be either 1 (for topographical gravitational field) or 2 (for RTM gravitational field)!');
end

if itype~=0 & itype~=1 & itype~=4 & itype~=2 & itype~=10 & itype~=103 & itype~=104
   error('Please check itype value: 0 for geoid; 1 for xi, eta and gravity disturbance; 4 for xi, eta and gravity anomaly; 2 for tensors; 10 for all components; 103 for geoid, xi,eta, gravity disturbance; 104 for geoid, xi, eta, gravity anomaly!');
end

if isempty(outname)
   c=clock; 
   outname=strcat(num2str(c(1)),num2str(c(2),'%02d'),num2str(c(3),'%02d'),'_',num2str(c(4),'%02d'),num2str(c(5),'%02d'),num2str(floor(c(4)),'%02d'),'.dat');
end

if find(outname=='.')>0
    output_report=outname(1:(find(outname=='.')-1));
else
    output_report=outname;
    outname=strcat(outname,'.dat');
end
output_report=strcat(output_report,'_report.txt');   

%constant parameter definition
Pi = 3.141592654;
G = 6.67384e-3;   % gravitational constant

%fid = fopen(ComPoints,'r');
%if  isempty(ComPoints)                   % evaluate ComPoints and make a statement
%    error('Undefined computation file.')
%end
%if fid<0
%    error('The defined computation file is non-existent.')
%end
%[filepath,name,ext] = fileparts(ComPoints);
%if ext == ".bin"

    %[data, cnt] = fread(fid, inf, 'double','native'); % format [N lat lon H]
    
    %nrow=cnt/4;
    %data=reshape(data,nrow,4);
    %com_lat=data(:,2);
    %com_lon=data(:,3);
    %com_h=data(:,4);   
    %num=data(end,1);	
%else
    data = ComPoints;
    com_lat=data(:,2);
    com_lon=data(:,3);
    if size(data,2)==4
        com_h=data(:,4);
    end
    
    num=size(data,1);
%end

%fclose(fid);
if exist("com_h")
    com_h(com_h<0)=1; % negative heights are corrected with positive constant height.
end
%H_com=com_h; 
% integration radius
r1 = rzones(1);            % radius for polyhedron
r2 = rzones(2);            % radius for prism
r3 = rzones(3);            % radius for tesseroid
r4 = rzones(4);            % radius for point-mass
clear data cnt

if r1>r2                   % r2>=r1
    r2=r1;
end


if r2>r3                   % r3>=r2
    r3=r2;
end

if r3>r4                   % r4>=r3
    r4=r3;
end
%% write output report --- parameter definition and computation point
fid=fopen(output_report,'w');
disp(output_report)
p='=========================    TGF_GUI project report   =========================';
formatSpec='%s\n';
fprintf(fid,formatSpec,p);
fprintf(fid,'\n');
fprintf(fid,'%30s','Generating_data:');
fprintf(fid,'%14s\n',datestr(now,1));
fprintf(fid,'%30s','Product_type:');
if ikind==1   
    fprintf(fid,'%35s\n','topographic gravitational field');
else if ikind==2
       fprintf(fid,'%26s\n','RTM gravitational field');
    end
end
fprintf(fid,'%30s','Functional:');
fprintf(fid,'%15s %d %s\n','itype=',itype,'(please refer to handbook)');
fprintf(fid,'%30s','Gravity field type:');
fprintf(fid,'%15s %d %s\n','ikind=',ikind,'(please refer to handbook)');
fprintf(fid,'%30s','Earth approximation:');
fprintf(fid,'%15s %d %s\n','iflag=',flag_earth,'(please refer to handbook)');
fprintf(fid,'%30s','Four zones radius [deg]:');
fprintf(fid,'%7.2f, %7.2f, %7.2f, %7.2f\n',rzones);
fprintf(fid,'\n');
p='----------------------  computation point information  -----------------------';
formatSpec='%s\n';
fprintf(fid,formatSpec,p);
fprintf(fid,'\n');
fprintf(fid,'%30s%10.4f\n','comlat_min:',min(com_lat)); 
fprintf(fid,'%30s%10.4f\n','comlat_max:',max(com_lat)); 
fprintf(fid,'%30s%10.4f\n','comlon_min:',min(com_lon)); 
fprintf(fid,'%30s%10.4f\n','comlon_max:',max(com_lon)); 
fprintf(fid,'%30s%10d\n','number of computation points:',num); 
fprintf(fid,'\n');
fclose(fid);            


%read DEM-- Detailed DEM 
%[minlat1, maxlat1, minlon1, maxlon1, reslat1, reslon1, z1] = read_DEM(DetailedDEM);
meta = DetailedDEM.meta;
[minlat1, maxlat1, minlon1, maxlon1, reslat1, reslon1] = deal(meta(1),meta(2),meta(3),meta(4),meta(5),meta(6));
z1 = DetailedDEM.DEM;
if minlat1>min(com_lat)-r2 | maxlat1<max(com_lat)+r2 | minlon1>min(com_lon)-r2 | maxlon1<max(com_lon)+r2
    error('Region of detailed DEM should be larger than integral area of innerzones!')
end

% read DEM -- tess DEM
%[minlatt, maxlatt, minlont, maxlont, reslatt, reslont, zt] = read_DEM(TessDEM);
meta = TessDEM.meta;
[minlatt, maxlatt, minlont, maxlont, reslatt, reslont] = deal(meta(1),meta(2),meta(3),meta(4),meta(5),meta(6));
zt = TessDEM.DEM;
if minlatt>min(com_lat)-r3 | maxlatt<max(com_lat)+r3 | minlont>min(com_lon)-r3 | maxlont<max(com_lon)+r3
    error('Region of detailed DEM should be larger than integral area of innerzones!')
end

%read DEM -- Coarse DEM
[minlat2, maxlat2, minlon2, maxlon2, reslat2, reslon2, z2] = read_DEM(CoarseDEM);     
if minlat2>min(com_lat)-r4 | maxlat2<max(com_lat)+r4 | minlon2>min(com_lon)-r4 | maxlon2<max(com_lon)+r4
    error('Region of coarse DEM should be larger than integral area of outerzone!')
end
%DEMs' coordinates
x1=minlon1:reslon1:maxlon1+1e-7;
y1=minlat1:reslat1:maxlat1+1e-7;
[x1,y1]=meshgrid(x1,y1);

xt=minlont:reslont:maxlont+1e-7;
yt=minlatt:reslatt:maxlatt+1e-7;
[xt,yt]=meshgrid(xt,yt);

x2=minlon2:reslon2:maxlon2+1e-7;
y2=minlat2:reslat2:maxlat2+1e-7;
[x2,y2]=meshgrid(x2,y2);

if exist("com_h")==0
    com_h=interp2(x1,y1,z1,com_lon,com_lat,'linear')+1;
end
H_com=com_h; 

if ikind==1         %Reference surface for topographic gravitational field, mean sea level
    [m, n]=size(x1);
    z1_ref(1:m,1:n)=0; 

    [m, n]=size(xt);
    zt_ref(1:m,1:n)=0; 
    
    [m, n]=size(x2);
    z2_ref(1:m,1:n)=0; 
else if ikind==2    %Reference surface for RTM gravitational field
        CoarseREF=strcat(CoarseREF);
        %DetailedREF=strcat(DetailedREF);
        %TessREF=strcat(TessREF);
        %[minlat1, maxlat1, minlon1, maxlon1, reslat1, reslon1, z1_ref] = read_DEM(DetailedREF);
        z1_ref = DetailedREF.DEM;
        [minlat2, maxlat2, minlon2, maxlon2, reslat2, reslon2, z2_ref] = read_DEM(CoarseREF);
        %[minlatt, maxlatt, minlont, maxlont, reslatt, reslont, zt_ref] = read_DEM(TessREF);
        zt_ref = TessREF.DEM;
     end
end

if ikind==1  %Global DEM and global REF for full-scale gravitational field calculation
    if ~isempty(GlobalDEM)
        [minyg, maxyg, minxg, maxxg, resolution_g, resolution_g, zg] = read_DEM(GlobalDEM); 
        [minyg, maxyg, minxg, maxxg, resolution_g, resolution_g, zg_ref] = read_DEM(GlobalREF);  
        xg=minxg:resolution_g:maxxg+1e-7;
        yg=minyg:resolution_g:maxyg+1e-7;
        [xg,yg]=meshgrid(xg,yg);
    end
else
    resolution_g =0; minyg =0 ; maxyg = 0 ; minxg = 0; maxxg = 0;xg =0;yg=0;zg =0;zg_ref=0;
end
        
% read mass-density model if idensity=1
if idensity==1
    if ~isempty(MassDensity)  %mass density fit with DetailedDEM and DetailedREF
        %[minlat1, maxlat1, minlon1, maxlon1, reslat1, reslon1, e_d] = read_DEM(MassDensity); 
        e_d = MassDensity.DEM;
    else
        [m, n]=size(z1);
        e_d(1:m,1:n)=e; 
    end
    
    if ~isempty(TessDensity)  %mass density fit with TessDEM and TessREF
        %[minlatt, maxlatt, minlont, maxlont, reslatt, reslont, e_t] = read_DEM(TessDensity);
        e_t = TessDensity.DEM;
    else
        [m, n]=size(zt);
        e_t(1:m,1:n)=e; 
    end
    
    if ~isempty(CoarseDensity)  %mass density fit with CoarseDEM and CoarseREF
        [minlat2, maxlat2, minlon2, maxlon2, reslat2, reslon2, e_c] = read_DEM(CoarseDensity);
    else
        [m, n]=size(z2);
        e_c(1:m,1:n)=e; 
    end
else
    [m, n]=size(z1);   % consatnt denisty mode
    e_d(1:m,1:n)=e;
    [m, n]=size(z2);
    e_c(1:m,1:n)=e;
    [m, n]=size(zt);
    e_t(1:m,1:n)=e; 
end   

%% write output report --- DEM and density information
fid=fopen(output_report,'a');
p='-------------------------------  DEM and Mass  -------------------------------';
formatSpec='%s\n';
fprintf(fid,formatSpec,p);
fprintf(fid,'\n');
fprintf(fid,'%30s %10.4f\n','DetailDEM_latlimit_north',maxlat1);
fprintf(fid,'%30s %10.4f\n','DetailDEM_latlimit_south',minlat1);
fprintf(fid,'%30s %10.4f\n','DetailDEM_reslat',reslat1);
fprintf(fid,'%30s %10.4f\n','DetailDEM_latlimit_west',minlon1);
fprintf(fid,'%30s %10.4f\n','DetailDEM_latlimit_est',maxlon1);
fprintf(fid,'%30s %10.4f\n','DetailDEM_reslon',reslon1);
fprintf(fid,'\n');
fprintf(fid,'%30s %10.4f\n','CoarseDEM_latlimit_north',maxlat2);
fprintf(fid,'%30s %10.4f\n','CoarseDEM_latlimit_south',minlat2);
fprintf(fid,'%30s %10.4f\n','CoarseDEM_reslat',reslat2);
fprintf(fid,'%30s %10.4f\n','CoarseDEM_latlimit_west',minlon2);
fprintf(fid,'%30s %10.4f\n','CoarseDEM_latlimit_est',maxlon2);
fprintf(fid,'%30s %10.4f\n','CoarseDEM_reslon',reslon2);
fprintf(fid,'\n');
if idensity==0
    fprintf(fid,'%30s %11s %10.4f %10s\n','Density_model:','const_e=',e,'[g/cm^3]');
else if idensity==1
        fprintf(fid,'%30s %50s','Density_model:','density_model shared the same boundary with DEM');
    end
end
fprintf(fid,'\n');
fclose(fid);

%%
         
%ellipsoidal parameters of GRS80 reference ellipsoid
d2r=Pi/180;
a=6378137;  %major radius
e_2=0.00673949677548; % eccentricity

%ellipsoidal radius of terrain model
 if flag_earth == 1
     re1=a./sqrt(1+e_2.*(sin(y1*d2r)).^2);  % ellipsoidal radius on the ellipsoidal surface of fine grid
     re2=a./sqrt(1+e_2.*(sin(y2*d2r)).^2);  % ellipsoidal radius on the ellipsoidal surface of coarse grid
     ret=a./sqrt(1+e_2.*(sin(yt*d2r)).^2);
     re_com=a./sqrt(1+e_2.*(sin(com_lat*d2r)).^2);
 else if flag_earth==0   % with spherical approximation
         re1=a;
         re2=a;
         ret=a;
         re_com=a;
     end
 end
z1=z1+re1;    % ellipsoidal radius of upper boundary
z2=z2+re2;    
zt=zt+ret;
z1_ref=z1_ref+re1;  % ellipsoidal radius of lower boundary
z2_ref=z2_ref+re2;
zt_ref=zt_ref+ret;

if ikind==1   % upper and lower boundaries of outest zone in full-scale gravitational field calculations
    if flag_earth==1
        reg=a./sqrt(1+e_2.*(sin(yg*d2r)).^2);
    else 
        reg=a;
    end
    zg=zg+reg;
    zg_ref=zg_ref+reg;
end

com_h=com_h+re_com;   % computation height

% define the resolution of each zone, resolution of oute zone must be
% integer times of resolution of the closest inner zone
resolution_y1=reslat1;
resolution_x1=reslon1;

resolution_y2=resolution_y1;
resolution_x2=resolution_x1;

resolution_y3=reslatt;
resolution_x3=reslont;

resolution_y4=reslat2;
resolution_x4=reslon2;


extend_x1=((floor(r1/resolution_x1)))*resolution_x1;
extend_y1=((floor(r1/resolution_y1)))*resolution_y1;

extend_x2=((floor(r2/resolution_x2)))*resolution_x2;
extend_y2=((floor(r2/resolution_y2)))*resolution_y2;

n1=resolution_x4/resolution_x3;
n2=resolution_y4/resolution_y3;
extend_x3=(n1*(floor(r3/resolution_x3/n1)))*resolution_x3;
extend_y3=(n2*(floor(r3/resolution_y3/n2)))*resolution_y3;

extend_x4=(4*(floor(r4/resolution_x4/4)))*resolution_x4;
extend_y4=(4*(floor(r4/resolution_y4/4)))*resolution_y4;


if ikind==2
    %size(x1),size(y1),size(z1_ref),size(com_lon),size(com_lat)
		ref_station=interp2(x1,y1,z1_ref,com_lon,com_lat,'cubic');         % height of computation point on reference surface
end

[m, n]=size(com_lon);
size_com=m*n;
% preallocating variables
  [V11, Vx11, Vy11, Vz11, Vxx11, Vxy11, Vxz11, Vyy11, Vyz11, Vzz11] = matrix_preallocating(1,size_com);
  [Vm, Vxm, Vym, Vzm, Vxxm, Vxym, Vxzm, Vyym, Vyzm, Vzzm] = matrix_preallocating(1,size_com);
  [V2, Vx2, Vy2, Vz2, Vxx2, Vxy2, Vxz2, Vyy2, Vyz2, Vzz2] = matrix_preallocating(1,size_com);
  [V3, Vx3, Vy3, Vz3, Vxx3, Vxy3, Vxz3, Vyy3, Vyz3, Vzz3] = matrix_preallocating(1,size_com); 
D = parallel.pool.DataQueue;
afterEach(D,@printIter);
IterFinished = 0;
% compute all ten gravity field elements
if itype==10
     t1=clock;
        text_process=strcat('is computing ten gravitational field elements for ',' ',num2str(size_com),' points \n');
    
    fprintf(text_process)
% polyhedron method for inner zone
    disp('Computing polyhedron method for inner zone (Fortran)\n')
    [V1, Vx1, Vy1, Vz1, Vxx1, Vxy1, Vxz1, Vyy1, Vyz1, Vzz1]=Polyhedron_full( x1,y1,z1,z1_ref,com_lat,com_lon,com_h,extend_x1,extend_y1,e_d,resolution_x1,resolution_y1);
    fprintf('Computing outer zones (pure Matlab code) \n')
    fprintf('%6d/%6d\n',[IterFinished,size_com])
    
    parfor i=1:size_com
        lon=com_lon(i);
        lat=com_lat(i);
        r0=com_h(i);
% build grid---first zone  
        n=find(x1<=lon+extend_x2 & x1>=lon-extend_x2 & y1<=lat+extend_y2 & y1>=lat-extend_y2);
        xm=x1(n);
        ym=y1(n);
        zm=z1(n);
        ref_m=z1_ref(n);
        e_m=e_d(n);
% leakage zone between zones of polyhedron and prism     
        if extend_x1>0 & extend_y1>0
            n=find(xm<=lon+extend_x1& xm>=lon-extend_x1& ym<=lat+extend_y1& ym>=lat-extend_y1);
            lon1=xm(n);
            lat1=ym(n);
            h1=zm(n);
            ref1=ref_m(n);
            e_zone1=e_m(n);
        
            xm(n)=[];
            ym(n)=[];
            zm(n)=[];
            ref_m(n)=[];
            e_m(n)=[];      
        
        
            lon_r=max(lon1);
            lon_l=min(lon1);
            lat_b=min(lat1);
            lat_t=max(lat1);

            n=find(lat1==lat_t | lat1==lat_b |lon1==lon_r |lon1==lon_l);
            lon1=lon1(n);
            lat1=lat1(n);
            h1=h1(n);
            ref1=ref1(n);
            e_zone1=e_zone1(n);

            xx1=lon1-resolution_x1/2;       
            xx2=lon1+resolution_x1/2;
            yy1=lat1-resolution_y1/2;
            yy2=lat1+resolution_y1/2;
            zz1=h1;
            zz2=ref1;
            [g, gx, gy, gz, gxx, gxy, gxz, gyy, gyz, gzz]=Prism(xx1,xx2,yy1,yy2,zz1,zz2,lon,lat,r0,e_zone1,resolution_x1,resolution_y1);  % prism field evaluation
            Vz11(i)=gz/2;
            Vx11(i)=gx/2;
            Vy11(i)=gy/2; 
            V11(i)=g/2;
            Vxx11(i)=gxx/2;
            Vxy11(i)=gxy/2;
            Vxz11(i)=gxz/2;
            Vyy11(i)=gyy/2;
            Vyz11(i)=gyz/2;
            Vzz11(i)=gzz/2;
        else
            n=find(xm<=lon+extend_x2& xm>=lon-extend_x2& ym<=lat+extend_y2& ym>=lat-extend_y2);
            lon1=xm(n);
            lat1=ym(n);
        end
% second zone 
        if(r2>r1)
            xx1=xm-resolution_x2/2;
            xx2=xm+resolution_x2/2;
            yy1=ym-resolution_y2/2;
            yy2=ym+resolution_y2/2;
            zz1=zm;
            zz2=ref_m;
            [g, gx, gy, gz, gxx, gxy, gxz, gyy, gyz, gzz]=Prism(xx1,xx2,yy1,yy2,zz1,zz2,lon,lat,r0,e_m,resolution_x2,resolution_y2);
            Vzm(i)=gz;
            Vm(i)=g;
            Vxm(i)=gx;
            Vym(i)=gy;
            Vxxm(i)=gxx;
            Vxym(i)=gxy;
            Vxzm(i)=gxz;
            Vyym(i)=gyy;
            Vyzm(i)=gyz;
            Vzzm(i)=gzz;
       end
 
% third zone 
       if(r3>r2)
           [lon2,lat2]=Build_grid(mean(lon1),mean(lat1),extend_x2,extend_y2,extend_x3,extend_y3,resolution_x3,resolution_y3);
           F=griddedInterpolant(xt',yt',zt','cubic');
