function [Vx, Vy, Vz]=g_prism2point(gx,gy,gz,lon,lat,prism_lon,prism_lat)
% In order to overcome the effect of Earth's curvature, transform the first derivatives of gravitational in prism-based 
% system to the station-based coordinate system.
Pi=3.141592654;
d2r = Pi/180;

prism_lon=d2r*prism_lon;
prism_lat=d2r*prism_lat;
lon=d2r*lon;
lat=d2r*lat;

cosbeta = cos((prism_lon - lon));
sinbeta = sin((prism_lon - lon));  % 20190623
cosphi = cos(lat);
sinphi = sin(lat);
cosphil = cos(prism_lat);
sinphil = sin(prism_lat);

cosbetasinphi= cosbeta.*sinphi;
cosbetacosphi= cosbeta.*cosphi;
R0 = cosbetasinphi.*sinphil + cosphi.*cosphil;
R1 = sinbeta.*sinphi;
R2 = -cosbetasinphi.*cosphil + cosphi.*sinphil;
R3 = -sinbeta.*sinphil;
R4= cosbeta;
R5 = sinbeta.*cosphil;
R6 = -cosbetacosphi.*sinphil + sinphi.*cosphil;
R7 = -sinbeta.*cosphi;
R8 = cosbetacosphi.*cosphil + sinphi.*sinphil;

Vx=R0.*gx+R1.*gy+R2.*gz;
Vy=R3.*gx+R4.*gy+R5.*gz;
Vz=R6.*gx+R7.*gy+R8.*gz;
end

