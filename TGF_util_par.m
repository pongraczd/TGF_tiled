function [gamma_h]=normal_gravity_generic(phi,h)
%% For calculation of normal gravity at station height, input latitde in deg, and geodetic height in meter
a=6378137;
b=6356752.3141;
gamma_a=9.7803267715;
gamma_b=9.8321863685;
m=0.00344978600308;

pi=3.141592654;
% conversion angle from deg to rad
phi = phi*pi/180;

% auxiliary quantities k,f,e2
k   = b*gamma_b/(a*gamma_a)-1;
f   = (a-b)/a;
a2=a*a;
e2  = (a2-b*b)/(a2);

% Normalschwere nach SOMIGLIANA, streng ------------------------------------	
% Torge 2002, p.95, Eq. 4.41b
sinphi=sin(phi);
gamma_0 = gamma_a * (1+k*sinphi.*sinphi)./sqrt(1-e2.*sinphi.*sinphi);

% Vertikaler Schweregradient, weitgehende Reihenentwicklung bis 2. Ordnung
% Torge 2002, p 98, Eq. 4.63
delta_gamma_h = gamma_0 .*( -2/a * ( 1 + f + m - 2*f*sinphi.*sinphi).* h  + 3/(a2)*h.*h);


gamma_h = gamma_0 + delta_gamma_h;
end

function [lon21,lat21]=Build_grid(lon,lat,extend_x1,extend_y1,extend_x2,extend_y2,resolution_x2,resolution_y2)
% grid building for each zone to avoid overlaping and leakage betwen adjacent zones 
% input: center of zone (lon, lat), inner and outer radius (extend_x1,extend_y1,extend_x2,extend_y2), grid resolution 
